                                <div class="mp-content-box-other&#95;_header-text">Proyek Wikimedia lain</div>
                            </span></h2>
                        <div class="mp-content-box-other&#95;_content"><b>Wikipedia bahasa Indonesia</b>,
                            ensiklopedia bebas dalam bahasa Indonesia, disediakan secara gratis oleh <a
                                href="/wiki/Wikimedia_Foundation" title="Wikimedia Foundation">Wikimedia
                                Foundation</a>, sebuah organisasi nirlaba. Selain dalam bahasa Indonesia,
                            Wikipedia tersedia dalam beberapa bahasa lain yang dipertuturkan di Indonesia: <a
                                href="https://ace.wikipedia.org/wiki/" class="extiw" title="ace:">Aceh</a>, <a
                                href="https://ban.wikipedia.org/wiki/" class="extiw" title="ban:">Bali</a>, <a
                                href="https://bjn.wikipedia.org/wiki/" class="extiw" title="bjn:">Banjar</a>, <a
                                href="https://map-bms.wikipedia.org/wiki/" class="extiw"
                                title="map-bms:">Banyumasan</a>, <a href="https://bbc.wikipedia.org/wiki/" class="extiw"
                                title="bbc:">Batak Toba</a>, <a href="https://bug.wikipedia.org/wiki/" class="extiw"
                                title="bug:">Bugis</a>, <a href="https://gor.wikipedia.org/wiki/" class="extiw"
                                title="gor:">Gorontalo</a>,
                            <a href="https://jv.wikipedia.org/wiki/" class="extiw" title="jv:">Jawa</a>, <a
                                href="https://mad.wikipedia.org/wiki/" class="extiw" title="mad:">Madura</a>, <a
                                href="https://ms.wikipedia.org/wiki/" class="extiw" title="ms:">Melayu</a>, <a
                                href="https://min.wikipedia.org/wiki/" class="extiw" title="min:">Minangkabau</a>, <a
                                href="https://nia.wikipedia.org/wiki/" class="extiw" title="nia:">Nias</a>, <a
                                href="https://su.wikipedia.org/wiki/" class="extiw" title="su:">Sunda</a>, dan <a
                                href="https://tet.wikipedia.org/wiki/" class="extiw" title="tet:">Tetun</a>.
